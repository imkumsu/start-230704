Java에는 기본 자료형과 참조 자료형의 두 가지 유형의 자료형이 있습니다.기본 자료형

- 불리언 자료형 : 참 또는 거짓을 저장할 수 있는 자료형입니다.
- 숫자 자료형
- 정수 자료형 : 정수를 저장할 수 있는 자료형입니다.
- short 자료형 : 2의 보수 16비트 정수를 저장할 수 있는 자료형입니다.
- int 자료형 : 2의 보수 32비트 정수를 저장할 수 있는 자료형입니다.
- long 자료형 : 2의 보수 64비트 정수를 저장할 수 있는 자료형입니다.
- 실수 자료형 : 실수를 저장할 수 있는 자료형입니다.
- float 자료형 : 32비트 단정밀도 부동 소수점을 저장할 수 있는 자료형입니다.
- double 자료형 : 64비트 배정밀도 부동 소수점을 저장할 수 있는 자료형입니다.
- 문자 자료형 : 문자를 저장할 수 있는 자료형입니다.
- char 자료형 : 유니코드 문자를 저장할 수 있는 자료형입니다.

참조 자료형

- 클래스 자료형 : 객체를 정의하는 자료형입니다.
- 인터페이스 자료형 : 클래스가 구현해야 하는 메소드의 목록을 정의하는 자료형입니다.
- 배열 자료형 : 같은 유형의 값의 컬렉션을 저장하는 자료형입니다.
- 열거형 자료형 : 고유한 값의 집합을 저장하는 자료형입니다.
- 자료형은 프로그램의 정확성과 효율성을 보장하는 데 중요합니다. 잘못된 자료형을 사용하면 프로그램이 실행되지 않거나 오작동할 수 있습니다.

